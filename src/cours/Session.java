package cours;

public class Session {
	private int idSession;
	private int anneeSession;
	private String libelSession;
	private String debutSession;
	private String dateFinSession;
	public int getIdSession() {
		return idSession;
	}
	public void setIdSession(int idSession) {
		this.idSession = idSession;
	}
	public int getAnneeSession() {
		return anneeSession;
	}
	public void setAnneeSession(int anneeSession) {
		this.anneeSession = anneeSession;
	}
	public String getLibelSession() {
		return libelSession;
	}
	public void setLibelSession(String libelSession) {
		this.libelSession = libelSession;
	}
	public String getDebutSession() {
		return debutSession;
	}
	public void setDebutSession(String debutSession) {
		this.debutSession = debutSession;
	}
	public String getDateFinSession() {
		return dateFinSession;
	}
	public void setDateFinSession(String dateFinSession) {
		this.dateFinSession = dateFinSession;
	}
	
	

}
